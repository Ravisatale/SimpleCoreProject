package com.qsp.SpringCoreXml;

public class Police {
private int id;
private String branch;
private int thief;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getBranch() {
	return branch;
}
public void setBranch(String branch) {
	this.branch = branch;
}
public int getThief() {
	return thief;
}
public void setThief(int thief) {
	this.thief = thief;
}
 
}
